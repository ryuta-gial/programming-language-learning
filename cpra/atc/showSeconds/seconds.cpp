#include <bits/stdc++.h>
using namespace std;
 
int main() {
  // 一年の秒数
  int seconds = 365 * 24 * 60 * 60;
  int twoyear = 365 * 2 * 24 * 60 * 60;
  int fiveyear = 365 * 5 * 24 * 60 * 60;
  int tenyear = 365 * 10 * 24 * 60 * 60;
 
  cout << seconds << endl;
  cout << twoyear << endl;
  cout << fiveyear << endl;
  cout << tenyear << endl;
}
